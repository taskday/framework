     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show(Comment $comment, string $uuid, Request $request)
    {
        /** @var Media[] $medias */
        $medias = $comment->getMedia()->filter(fn ($item) => $item->uuid == $uuid);

        if (count($medias) == 0) {
            abort(404);
        }

        return $medias[0]->toResponse($request);
    }

